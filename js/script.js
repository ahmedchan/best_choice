	var FormSteps = FormSteps || {
		_init : function(){
			var self   = this;

			self.$win  = $(window); 
			self.$doc  = $(document);
			self.$body = $(document.body);

			//methods
			self.cacheSelectors();
			self._initEvents();

			this.panels.hide();
			this.panels.first().show();

		},

		cacheSelectors : function(){
			this.container = $('#wizard');
			this.nextBtn   = this.container.find('button.nextBtn');
			this.nav       = this.container.find('div.setup-panel div a');
			this.panels    = this.container.find('div.setup-content');
			this.formGroup = this.container.find('div.form-group');
		},

		_initEvents : function(){
			this.nav.on( 'click', $.proxy(this.goToNext, this) );
			this.nextBtn.on( 'click', this.validateNext );
		},

		goToNext: function(e){
			e.preventDefault();
			var _self = $(e.target), $target = $(_self.attr('href'));
			
			if( !_self.attr('disabled') ){
				this.nav.removeClass('primary').addClass('default');
				_self.addClass('primary').next('p').removeClass('disabled');

				this.panels.hide();
				$target.show().find('input:eq(0)').focus();
			}
		},

		validateNext : function(e){
			var _self = $(this),
				curStep = _self.closest(".setup-content"),
				curStepBtn = curStep.attr("id"),
				nextStep = $('.setup-panel div a[href="#' + curStepBtn + '"]').parent().next().find("a"),
				curInputs = curStep.find(".form-control"),
				isValid = true,
				i=0, len = curInputs.length;

			$(".form-group").removeClass("has-error");

			for( i; i < len; i++ ){
            if (!curInputs[i].validity.valid){
               isValid = false;
               $(curInputs[i]).closest(".form-group").addClass("has-error");
            }
        	}

        	if (isValid)
            nextStep.removeAttr('disabled').trigger('click');
		}
	};

	// call to active FormSteps...
	FormSteps._init();

